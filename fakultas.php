                    <h3>Fakultas Ilmu Sosial dan Ilmu Budaya</h3>
                    <p><strong>A. Sejarah Singkat Fakultas Ilmu Sosial dan Ilmu Budaya ( FISIB )</strong></p>
              <p>Fakultas Ilmu Sosial dan Ilmu Budaya (FISIB) Universitas Trunojoyo Madura berdiri pada tanggal 18 Januari 2008, berdasarkan Hasil Pengesahan Rapat Senat Universitas. Latar belakang berdirinya FISIB ini berangkat dari pertimbangan kelangsungan dan masa depan jurusan Ilmu Komunikasi, Sosiologi, dan Psikologi yang pada saat itu masih berada di bawah naungan Fakultas Hukum. Fakultas Hukum yang bersifat monoprogram sangat tidak mungkin menjadi nduk dari ketiga disiplin ilmu tersebut, dan terus menerus harus menaungi secara administratif.</p>
              <p>Selain itu, kelangsungan dan perkembangan Program Studi Sastra Inggris juga menjadi pertimbangan, mengingat pada saat itu Program Studi Sastra Inggris masih berada di bawah naungan PR-1. Atas pertimbangan masa depan dan perkembangan keempat program studi ilmu sosial tersebut, maka didirikanlah Fakultas Ilmu Sosial dan Ilmu Budaya (FISIB), sebagai wadah ilmu-ilmu sosial yang berada di lingkungan Universitas Trunojoyo Madura.</p>
              <p><strong>B. Visi dan Misi</strong></p>
              <p>Visi :</p>
              <p>Menjadikan Fakultas Ilmu Sosial dan Ilmu Budaya (FISIB) Universitas Trunojoyo Madura (UTM) sebagai institusi yang unggul di bidang Pendidikan, Penelitian dan Pengabdian kepada Masyarakat dalam Ilmu Sosial dan Ilmu Budaya.</p>
              <p>Misi :</p>
              <p>1. Menyelenggarakan pendidikan yang bermutu, kontekstual dan berkarakter.</p>
              <p>2. Melaksanakan, mengembangkan dan meningkatkan penelitian sesuai dengan bidang sosial dan budaya.</p>
              <p>3. Melaksanakan pengabdian kepada masyarakat yang berdaya guna bagi pemberdayaan masyarakat.</p>
              <p>4. Menggali dan mengembangkan kearifan lokal sebagai upaya pelestarian nilai - nilai sosial budaya.</p>
              <p><strong>C. Jaringan Kerjasama</strong></p>
