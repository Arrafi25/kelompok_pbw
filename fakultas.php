                <h3>Fakultas Teknik</h3>
                <p><strong>A. Visi dan Misi</strong></p>
              <p>Visi</p>
              <p>Akhir tahun 2022, Fakultas Teknik Universitas Trunojoyo Madura menjadi Fakultas yang unggul di tingkat Nasional dalam Bidang Pendidikan dan Riset<em>Engineering</em>.</p>
              <p>Misi</p>
              <p>1.Menyelenggarakan pendidikan yang berkualitas untuk menghasilkan lulusan, ilmu pengetahuan dan teknologi yang sesuai dengan kebutuhan<em>stakeholder</em>;</p>
              <p>2.Melakukan penelitian untuk mendukung peningkatan kualitas pendidikan, ilmu pengetahuan dan teknologi;</p>
              <p>3.Melaksanakan pengabdian kepada masyarakat berbasis ilmu pengetahuan, teknologi dan hasil penelitian untuk memberikan kontribusi bagi masyarakat.</p>
              <p><strong>Tujuan Fakultas Teknik Universitas Trunojoyo Madura</strong></p>
              <p>1.Menghasilkan lulusan yang mampu memenuhi kebutuhan<em>stakeholder</em>nya;</p>
              <p>2.Membentuk sistem pendidikan yang handal (rebiable) untuk mengembangkan ilmu dan pengetahuan dalam bidang<em>engineering</em>yang sesuai dengan kebutuhan stakeholder;</p>
              <p>3.Meningkatkan hasil karya penelitian dalam bentuk publikasi ilmiah dan terapan teknologi yang bermamfaat bagi civitas akademik dan masyarakat;</p>
              <p>4.Membangun dan meningkatkan kerjasama dengan stakeholder untuk meningkatkan kualitas pendidikan,penelitian dan pengabdian pada masyarakat;</p>
              <p>5.Menghasilkan jalinan kerja sama yang senergis dan berkelanjutan.</p>
              <p><strong>B. Sasaran dan strategi pencapaiannya</strong></p>
              <p>Strategi pencapaian visi, misi dan tujuan Fakultas Teknik tertuang dalam dokumen<strong>Renstra 2018-2022</strong>dan<strong>Rencana Operasional (Renop) Fakultas Teknik Universitas Trunojoyo Madura 2018-2022</strong>yang disahkan dengan<strong>SK Dekan No. 75/UN46.1.31/2018</strong>pada tanggal 23 April 2018. Adapun sasaran dan strategi pencapaian serta indikator kinerja Fakultas Teknik Universitas Trunojoyo Madura adalah sebagai berikut;</p>
              <p>Mengacu pada Milestone pencapaian visi dan misi UTM (Gambar 1.1). Saat ini Fakultas Teknik Universitas Trunojoyo Madura dalam pencapaian sasaran dan strategi yang dilakukan dengan memprioritaskan pada optimalisasi manajemen mutu dan pemenuhan standar mutu Fakultas serta peningkatan kerjasama yang dituangkan pada Gambar 1.2.</p>
              <p><img style="width: 300px; height: 200px;" src="https://www.trunojoyo.ac.id/images/stories/A.png" alt="gambar1" /></p>
              <p>Gambar 1.1.<em>Milestone</em>Pencapaian Visi Misi UTM</p>
              <p><img style="width: 300px; height: 200px;" src="https://www.trunojoyo.ac.id/images/stories/B.png" alt="gambar2" /></p>
              <p>Gambar 1.2.<em>Milestone</em>Pencapaian Visi Misi Fakultas TeknikUniversitas Trunojoyo Madura</p>
              <p><strong>Program 1 : Program Peningkatan Kompetensi Lulusan / Mahasiswa.</strong></p>
              <p>1.Peningkatan daya panggil dan promosi, indikator kinerja yaitu rasio jumlah calon mahasiswa yang ikut seleksi terhadap daya panggil meningkat.</p>
              <p>2.Peningkatan jumlah mahasiswa yang mampu mencapai skor TOEFL 450, indikator kinerja yaitu mahasiswa yang akan mengikuti proses Yusidium wajib memiliki skor TOEFL 450.</p>
              <p>3.Aktivitas pengembangan organisasi mahasiswa, indikator kinerja yaitu aktivitas dan jumlah kegiatan organisasi mahasiswa di Fakultas Teknik bertambah</p>
              <p>4.Kegiatan pengembangan minat dan bakat mahasiswa, indikator kinerja yaitu jumlah kegiatan pengembangan minat dan bakat mahasiswa selalu meningkat setiap tahun.</p>
              <p>5.Pelatihan kewirausahaan, indikator kinerja yaitu jumlah pelatihan kewirausahaan meningkat setiap tahun</p>
              <p>6.Pelatihan kepemimpinan, indikator kinerja yaitu pelatihan kepemimpinan rutin diadakan setiap tahun</p>
              <p>7.Dukungan institusi terhadap mahasiswa yang mengikuti lomba/kejuaraan tingkat regional, nasional dan internasional (persiapan materi, fasilitas dan keuangan), indikator kinerja yaitu prestasi mahasiswa di tingkat regional dan nasional dapat diraih setiap tahun.</p>
              <p>8.Memberikan layanan pembinaan karir mahasiswa, indikator kinerja yaitu memberikan pembekalan<em>soft skill</em>bagi mahasiswa tingkat akhir secara rutin setiap tahun dan kegiatan wirausaha mahasiswa meningkat</p>
              <p>9.<em>Workshop</em>kurikulum berbasis KKNI, indikator kinerja yaitu semua prodi menerapkan kurikulum KKNI.</p>
              <p>10.Penyusunan RPS berbasis kurikulum KKNI, indikator kinerja yaitu tersusunnya kurikulum berbasis<em>learning outcome</em>yang mengacu pada KKNI.</p>
              <p>11.Hibah pembuatan bahan ajar, indikator kinerja yaitu persentase mata kuliah yang telah memiliki buku ajar,<em>hand out</em>dan diktat meningkat.</p>
              <p>12.Meningkatkan kualitas dan kuantitas lulusan mahasiswa, indikator kinerja yaitu masa tempuh studi, IPK rata-rata, dan produktivitas lulusan mahasiswa semakin baik.</p>
              <p>13.Menyediakan layanan perwalian bagi mahasiswa, indikator kinerja yaitu persentase angka DO/keluar mahasiswa menurun.</p>
              <p><strong>Program 2 : Program Peningkatan Kualitas dan Kuantitas Dosen dan Tenaga Kependidikan</strong></p>
              <p>1.Pengiriman dosen studi lanjut, indikator kinerja yaitu persentase dosen yang berkualifikasi Doktor (S3) meningkat</p>
              <p>2.Pemetaan kompetensi dosen dan perencanaan kebutuhan, indikator kinerja yaitu tersedia peta kompetensi dan kebutuhan dosen.</p>
              <p>3.Pengadaan petugas ruang baca, indikator kinerja yaitu tersedianya 1 orang petugas ruang baca.</p>
              <p>4.Peningkatan kualitas dosen dan Tenaga Kependidikan, indikator kinerja yaitu jumlah dosen dan tenaga kependidikan yang mengikuti program pelatihan keahlian tersertifikasi dan relevan dengan bidang keahlian dan kebutuhan pasar kerja bagi lulusan setiap tahun meningkat.</p>
              <p><strong>Program 3 : Program Pengembangan Sarana dan Prasarana Penunjang Pendidikan</strong></p>
              <p>1.Pemetaan ketersediaan dan kebutuhan buku sesuai disiplin ilmu, indikator kinerja yaitu kecukupan buku tiap program studi meningkat.</p>
              <p>2.Hibah sarana penunjang pendidikan, indikator kinerja yaitu peningkatan jumlah hibah buku ajar dan adanya pengelolaan jurnal ilmiah yang terakreditasi.</p>
              <p>3.Pengadaan perangkat keras dan lunak untuk mendukung ketersediaan jurnal internasional, indikator kinerja yaitu memiliki 1 server untuk menyimpan data e-jurnal yang diunduh.</p>
              <p>4.Evaluasi kebutuhan peralatan fungsional, indikator kinerja yaitu persentase kelengkapan peralatan laboratorium memadai.</p>
              <p>5.Perbaikan alat fungsional, indikator kinerja yaitu kondisi peralatan fungsional menjadi lebih baik.</p>
