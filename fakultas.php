                  <p>Perkembangan kehidupan masyarakat pada era global saat ini, menuntut perguruan tinggi untuk melakukan reorientasi reformasi dalam sistem pendidikan, keseimbangan dalam sosial. Hal itu merupakan bagian terpenting dari perguruan tinggi sebagai lembaga pengajaran, pencipta pengetahuan dan penyedia yang berdaya guna dalam peningkatan kesejahteraan rakyat dan daya saing bangsa. Dengan demikian, berbagai Prodi harus berupaya meningkatkan kualitas pendidikannya kepada masyarakat. Upaya yang harus dilakukan oleh UTM adalah menyeimbangkan pengembangan ilmu, baik eksakta, sosial, budaya, dan agama di dalam sistem pendidikan. Pengembangan berbagai Prodi eksakta telah membentuk fakultas Pertanian (FP) dan Fakultas Teknik (FT). Adapun pengembangan ilmu sosial dan budaya, telah dilakukan UTM melalui Prodi  Prodi dibawah fakultas Hukum (FH), Fakultas Ekonomi (FE) dan Fakultas Ilmu Sosial Dan Budaya (FISIB).</p>
